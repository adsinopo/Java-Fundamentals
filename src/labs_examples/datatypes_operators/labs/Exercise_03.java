/**
 * Please demonstrate the use of all arithmetic operators below. These include:
 *
 * addition, subtraction, multiplication, division and modulus
 *
 */
class ArithmeticOperators {

    public static void main(String[] args) {

        // write your code below
        int addition = 2 + 10;
        System.out.println(addition); //prints 12
        int subtract = 14 - 3;
        System.out.println(subtract); //prints 11
        int multiply = 6 * 7;
        System.out.println(multiply); //prints 42
        int divide = 35 / 5;
        System.out.println(divide); //prints 7
        int mod = 14 % 4;
        System.out.println(mod); //prints 2

    }

}
